L[1][1] = 1
L[1][2] = 0
L[1][3] = 0
L[2][1] = 0
L[2][2] = 1
L[2][3] = 0
L[3][1] = 1
L[3][2] = 1
L[3][3] = 1
p = 4
T= int(input())

for i in range(T):
    n = int(input())
    if(L[n][1]+ L[n][2]+ L[n][3] == 0):
        while(p<=n):
            L[p][1] = (L[p-1][2] + L[p-1][3])% 1000000009
            L[p][2] = (L[p-2][1] + L[p-2][3])% 1000000009
            L[p][3] = (L[p-3][1] + L[p-3][2])%1000000009
            p+=1
        print((L[n][1]+L[n][2]+L[n][3]) % 1000000009)
    else:
        print((L[n][1]+L[n][2]+L[n][3]) % 1000000009)
